def report_to_docx(startup: dict, report: dict) -> bytes:
    """Generate a DOCX document from a startup analysis report."""
    try:
        from docx import Document  # type: ignore
        from docx.shared import Pt, RGBColor  # type: ignore
        from docx.enum.text import WD_ALIGN_PARAGRAPH  # type: ignore
    except ImportError as exc:
        raise RuntimeError(
            "python-docx is not installed. Run: pip install python-docx"
        ) from exc

    doc = Document()

    # Title
    title_para = doc.add_heading(startup.get("startup_name", "Startup Analysis"), 0)
    title_para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # Meta info table
    doc.add_heading("Startup Overview", level=1)
    table = doc.add_table(rows=1, cols=2)
    table.style = "Light Grid Accent 1"
    meta_fields = [
        ("Founder", startup.get("founder_name", "")),
        ("Industry", startup.get("industry", "")),
        ("Country", startup.get("country", "")),
        ("Budget", startup.get("budget", "")),
        ("Status", startup.get("status", "").title()),
        ("Created", startup.get("created_at", "")),
    ]
    hdr = table.rows[0].cells
    hdr[0].text = "Field"
    hdr[1].text = "Value"
    for field, value in meta_fields:
        row = table.add_row()
        row.cells[0].text = field
        row.cells[1].text = str(value)

    doc.add_paragraph()

    # Scores
    if report:
        doc.add_heading("AI Scores", level=1)
        scores_table = doc.add_table(rows=1, cols=2)
        scores_table.style = "Light Grid Accent 1"
        sh = scores_table.rows[0].cells
        sh[0].text = "Dimension"
        sh[1].text = "Score"
        for dim, key in [
            ("Viability", "viability_score"),
            ("Market Fit", "market_score"),
            ("Innovation", "innovation_score"),
            ("Execution", "execution_score"),
        ]:
            row = scores_table.add_row()
            row.cells[0].text = dim
            row.cells[1].text = f"{report.get(key, 0)}%"
        doc.add_paragraph()

        # Full report content
        doc.add_heading("Full Analysis Report", level=1)
        content = report.get("content", "")
        _add_markdown_content(doc, content)

    # Idea description
    doc.add_heading("Original Idea Description", level=1)
    doc.add_paragraph(startup.get("idea_description", ""))

    doc.add_paragraph()
    footer_para = doc.add_paragraph(
        f"Generated by AI Startup Incubator Agent | {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}"
    )
    footer_para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    buf = io.BytesIO()
    doc.save(buf)
    buf.seek(0)
    return buf.read()


