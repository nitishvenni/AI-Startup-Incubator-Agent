def report_to_pdf(startup: dict, report: dict) -> bytes:
    """
    Generate Startup Analysis PDF using ReportLab.
    """

    buffer = io.BytesIO()

    doc = SimpleDocTemplate(buffer)

    styles = getSampleStyleSheet()

    story = []

    story.append(
        Paragraph(
            f"<b><font size=20>{startup.get('startup_name','Startup Analysis')}</font></b>",
            styles["Title"],
        )
    )

    story.append(Spacer(1, 0.3 * inch))

    story.append(
        Paragraph(
            f"<b>Founder:</b> {startup.get('founder_name','')}",
            styles["Normal"],
        )
    )

    story.append(
        Paragraph(
            f"<b>Industry:</b> {startup.get('industry','')}",
            styles["Normal"],
        )
    )

    story.append(
        Paragraph(
            f"<b>Country:</b> {startup.get('country','')}",
            styles["Normal"],
        )
    )

    story.append(
        Paragraph(
            f"<b>Budget:</b> {startup.get('budget','')}",
            styles["Normal"],
        )
    )

    story.append(Spacer(1, 0.25 * inch))

    story.append(Paragraph("<b>AI Analysis Scores</b>", styles["Heading2"]))

    story.append(
        Paragraph(
            f"Viability: {report.get('viability_score',0)}%",
            styles["Normal"],
        )
    )

    story.append(
        Paragraph(
            f"Market: {report.get('market_score',0)}%",
            styles["Normal"],
        )
    )

    story.append(
        Paragraph(
            f"Innovation: {report.get('innovation_score',0)}%",
            styles["Normal"],
        )
    )

    story.append(
        Paragraph(
            f"Execution: {report.get('execution_score',0)}%",
            styles["Normal"],
        )
    )

    story.append(Spacer(1, 0.3 * inch))

    story.append(
        Paragraph("<b>Startup Analysis</b>", styles["Heading2"])
    )

    content = report.get("content", "")

    for line in content.split("\n"):

        line = line.strip()

        if not line:
            continue

        story.append(
            Paragraph(line.replace("\n", "<br/>"), styles["BodyText"])
        )

    story.append(Spacer(1, 0.3 * inch))

    story.append(
        Paragraph("<b>Original Idea</b>", styles["Heading2"])
    )

    story.append(
        Paragraph(startup.get("idea_description", ""), styles["BodyText"])
    )

    story.append(Spacer(1, 0.3 * inch))

    story.append(
        Paragraph(
            f"Generated by AI Startup Incubator Agent<br/>{datetime.now()}",
            styles["Italic"],
        )
    )

    doc.build(story)

    pdf = buffer.getvalue()

    buffer.close()

    return pdf
    """
    Render a startup analysis report as a PDF and return raw bytes.
    Uses WeasyPrint (HTML → PDF pipeline).
    """
    try:
        from weasyprint import HTML, CSS  # type: ignore
    except ImportError as exc:
        raise RuntimeError(
            "WeasyPrint is not installed. Run: pip install weasyprint"
        ) from exc

    html_str = _build_report_html(startup, report)
    try:
        pdf_bytes = HTML(string=html_str, base_url=None).write_pdf()
        return pdf_bytes
    except Exception as exc:
        logger.error("PDF generation failed: %s", exc)
        raise RuntimeError(f"PDF generation failed: {exc}") from exc





